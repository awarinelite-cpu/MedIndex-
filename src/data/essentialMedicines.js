// Normalize a drug/ingredient name for matching: lowercase, strip
// parenthetical abbreviations/salts (e.g. "(ABC)", "(hydrochloride)"),
// collapse combination-drug separators ("+", "/") and punctuation/spacing
// so equivalent forms compare equal.
function normalizeName(name) {
  let n = (name || '').toLowerCase();
  n = n.replace(/\([^)]*\)/g, ' ');           // drop parentheticals
  n = n.replace(/[+/]/g, ' ');                 // combo separators -> space
  n = n.replace(/[.,]/g, ' ');
  n = n.replace(/\b(hydrochloride|hcl|sodium|potassium|sulfate|sulphate|phosphate|maleate|mesylate|besylate|succinate|tartrate|dihydrate|monohydrate|citrate|acetate|trihydrate|hydrogen)\b/g, ' ');
  n = n.replace(/\s+/g, ' ').trim();
  return n;
}

let _essentialSet = null;
function getEssentialSet() {
  if (!_essentialSet) {
    _essentialSet = new Set(NEML_8TH_EDITION_NAMES.map(normalizeName));
  }
  return _essentialSet;
}

// True if this drug's generic_name matches an entry on the NEML 8th Edition
// (2024). Combination products (e.g. "Amoxicillin + Clavulanic Acid") match
// as a whole; a drug that is only ONE component of a listed combination
// (e.g. plain "Amoxicillin" vs the NEML's "Amoxicillin + Clavulanic Acid"
// entry) is also matched, since each ingredient is separately essential in
// its own single-agent NEML entry in the vast majority of cases.
export function isEssentialDrug(drug) {
  if (!drug || !drug.generic_name) return false;
  const essential = getEssentialSet();
  const normalized = normalizeName(drug.generic_name);
  if (!normalized) return false;
  if (essential.has(normalized)) return true;
  // Also try matching just the first component of a combination name
  // against the essential set (handles cases where MedIndex splits a
  // combination differently than the NEML does).
  const firstPart = normalized.split(' ')[0];
  return NEML_8TH_EDITION_NAMES.some(n => {
    const nn = normalizeName(n);
    return nn === normalized || (nn.split(' ')[0] === firstPart && firstPart.length > 3 && nn.startsWith(firstPart));
  });
}
